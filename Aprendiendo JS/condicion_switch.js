// Sentencia SWITCH 
//Comprobar condiciones de manera mas legible (Similar IF /ELIF /ELSE)
//El resultado ser√° 7

let operacion = 5 + 2 

switch (operacion) {
  case 0:
    console.log("El resultado es 0")
    break; 
  case 7:
    console.log("El resultado es 7")
    break;
  default:
    console.log("El resultado no es 0 ni 7")
}

